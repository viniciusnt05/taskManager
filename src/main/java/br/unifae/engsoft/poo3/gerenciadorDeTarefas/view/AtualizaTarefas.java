    private void jbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarActionPerformed
          try {
            String descricao = jtxtDescricao.getText();
            int prioridade = Integer.parseInt((String) jcomboPrioridade.getSelectedItem());
            String dataCriacao = jtxtCriacao.getText();
            String prazoString = jtxtPrazo.getText();
            boolean concluida = jcheckConcluida.isSelected();

            boolean atualizada = controller.atualizarTarefa(tarefaSelecionada, descricao, prioridade, dataCriacao, prazoString, concluida);

            if (atualizada) {
                JOptionPane.showMessageDialog(rootPane, "Tarefa atualizada com sucesso!");
                abrirTabelaTarefas();
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar a tarefa.");
            }

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_jbtnSalvarActionPerformed

