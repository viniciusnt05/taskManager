    // Função para os campos já virem preenchidos e o usuário atualizar apenas o que ele quiser (em vez de ter que preencher tudo de novo)
    private void preencherCampos() {
        if (tarefaSelecionada != null) {
            jtxtDescricao.setText(tarefaSelecionada.getDescricao());
            jcomboPrioridade.setSelectedItem(String.valueOf(tarefaSelecionada.getPrioridade()));
            jtxtCriacao.setText(tarefaSelecionada.getDataCriacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
            jcheckConcluida.setSelected(tarefaSelecionada.isConcluida());

            if (tarefaSelecionada instanceof TarefaComPrazo) {
                LocalDate prazo = ((TarefaComPrazo) tarefaSelecionada).getPrazo();
                jtxtPrazo.setText(prazo.format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
            }
        }
    }

    private void abrirTabelaTarefas() {
        TabelaTarefas tabelaTarefas = new TabelaTarefas(controller);
        tabelaTarefas.setVisible(true);
        tabelaTarefas.atualizarTabela();
    }

